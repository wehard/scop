#include "vec3.h"

t_vec3	ft_cross_vec3(t_vec3 a, t_vec3 b)
{
	t_vec3 c;

	c.x = a.y * b.z - a.z * b.y;
	c.y = a.z * b.x - a.x * b.z;
	c.z = a.x * b.y - a.y * b.x;
	return (c);
}

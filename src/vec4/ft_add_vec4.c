#include "vec4.h"

t_vec4	ft_add_vec4(t_vec4 a, t_vec4 b)
{
	t_vec4	c;

	c.x = a.x + b.x;
	c.y = a.y + b.y;
	c.z = a.z + b.z;
	c.w = a.w + b.w;
	c.w /= c.w;
	return (a);
}

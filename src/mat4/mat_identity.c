#include "mat4.h"

t_mat4 mat4_identity(void)
{
	t_mat4 m;

	m.data[0] = 1.0;
	m.data[1] = 0.0;
	m.data[2] = 0.0;
	m.data[3] = 0.0;

	m.data[4] = 0.0;
	m.data[5] = 1.0;
	m.data[6] = 0.0;
	m.data[7] = 0.0;

	m.data[8] = 0.0;
	m.data[9] = 0.0;
	m.data[10] = 1.0;
	m.data[11] = 0.0;

	m.data[12] = 0.0;
	m.data[13] = 0.0;
	m.data[14] = 0.0;
	m.data[15] = 1.0;
	return (m);
}

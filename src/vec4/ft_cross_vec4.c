#include "vec4.h"

t_vec4	ft_cross_vec4(t_vec4 a, t_vec4 b)
{
	t_vec4 c;

	c.x = a.y * b.z - a.z * b.y;
	c.y = a.z * b.x - a.x * b.z;
	c.z = a.x * b.y - a.y * b.x;
	c.w = 1.0;
	return (c);
}
